func Supported() []Lang {
	return languages
}

func IsSupported(lang string) bool {
	_, ok := translations[lang]
	return ok
}

func T(lang string, key string) string {
	lang = strings.ToLower(lang)
	if lang == "" {
		lang = "en"
	}
	if bucket, ok := translations[lang]; ok {
		if value, ok := bucket[key]; ok {
			return value
		}
	}
	if bucket, ok := translations["en"]; ok {
		if value, ok := bucket[key]; ok {
			return value
		}
	}
	return key
}
